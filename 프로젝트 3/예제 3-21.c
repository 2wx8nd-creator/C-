#include <stdio.h> /*논리 연산자 &&와 ||, 논리 부정 연산자 !*/

int main(void) {
	int i = 10;

	printf("%d\n", 1 < i && i < 2); // 참 && 거짓 ... 거짓 
	printf("%d\n", 1 < i && i < 11); // 참 && 참 ... 참
	
	
	printf("%d\n", 1 < i < 2); // 참 < 2 ... 1 < 2 ... 참
	printf("%d\n", 1 < i > 2); // 참 > 2 ... 1 > 2 ... 거짓
	/* 좌결합성을 명심하라 ... */

}